def _load_weights() -> dict[str, float]:
    """Load skill weights from skill_router_weights.json if present.

    Returns a dict mapping skill slug -> float weight (default 1.0 if absent).
    Weights are generated by skill-router-weight-updater.py from telemetry.
    """
    if not WEIGHTS_FILE.is_file():
        return {}
    try:
        data = json.loads(WEIGHTS_FILE.read_text(encoding="utf-8"))
        weights = data.get("weights", {})
        # Validate: weights must be floats in [0.1, 2.0]
        return {
            k: float(max(0.1, min(2.0, v)))
            for k, v in weights.items()
            if isinstance(k, str) and isinstance(v, (int, float))
        }
    except (json.JSONDecodeError, OSError, ValueError):
        return {}


